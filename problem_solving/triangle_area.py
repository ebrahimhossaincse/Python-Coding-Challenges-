# Program to calculate the area of a triangle
# Uses the formula: Area = (1/2) * base * height

# Get input from the user
base = float(input("Enter the base of the triangle: "))  # Base input
height = float(input("Enter the height of the triangle: "))  # Height input

# Calculate the area
area = 0.5 * base * height

# Display the result
print(f"The area of the triangle is: {area}")